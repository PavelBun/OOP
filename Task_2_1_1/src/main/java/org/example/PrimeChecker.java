package org.example;

public abstract class PrimeChecker {
    public static boolean isPrime(int n) {
        if (n < 2) {
            return false;
        }
        for (int i = 2; i * i <= n; i++) {
            if (n % i == 0) {
                return false;
            }
        }
        return true;
    }
    public abstract boolean isContain() throws InterruptedException;

}